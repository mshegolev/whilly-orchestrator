"""Generate local Docker env files and print a runnable Whilly setup runbook."""

from __future__ import annotations

import argparse
import platform
import secrets
import shutil
import subprocess
import sys
from collections.abc import Callable, Sequence
from datetime import datetime, timezone
from pathlib import Path

UTC = timezone.utc

try:
    from whilly import __version__
except Exception:  # pragma: no cover - defensive fallback for source-tree use.
    __version__ = "dev"


EXIT_OK = 0
EXIT_USAGE = 2

CONTROL_COMPOSE_FILE = "docker-compose.control-plane.yml"
WORKER_COMPOSE_FILE = "docker-compose.worker.yml"

_MIN_PYTHON_MAJOR = 3
_MIN_PYTHON_MINOR = 12
_PYTHON_REQUIRED = f"python{_MIN_PYTHON_MAJOR}.{_MIN_PYTHON_MINOR}"

_REDACTED_KEYS = frozenset(
    {
        "JIRA_API_TOKEN",
        "POSTGRES_PASSWORD",
        "WHILLY_METRICS_TOKEN",
        "WHILLY_WORKER_BOOTSTRAP_TOKEN",
        "ANTHROPIC_API_KEY",
        "OPENROUTER_API_KEY",
        "OPENAI_API_KEY",
        "GROQ_API_KEY",
        "GEMINI_API_KEY",
        "LLM_API_KEY",
    }
)
_GENERATED_SECRET_KEYS = frozenset({"POSTGRES_PASSWORD", "WHILLY_WORKER_BOOTSTRAP_TOKEN"})


def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="whilly quick-setup",
        description="Generate local .env files and print Docker startup commands.",
    )
    parser.add_argument("--repo-root", default=".", help="Repo root to write .env files into. Default: current dir.")
    parser.add_argument("--yes", action="store_true", help="Write files without an interactive confirmation.")
    parser.add_argument("--force", action="store_true", help="Overwrite existing .env and .env.worker files.")
    parser.add_argument("--print-only", action="store_true", help="Print the generated files/runbook without writing.")
    parser.add_argument("--show-secrets", action="store_true", help="Print generated secret values in the terminal.")
    parser.add_argument(
        "--compose-command",
        default=None,
        help="Compose command to print, for example 'docker compose' or 'docker-compose'.",
    )
    parser.add_argument(
        "--docker-provider",
        choices=("auto", "colima", "desktop", "linux"),
        default="auto",
        help="Docker provider guidance to print. Default: auto.",
    )
    parser.add_argument("--image", default=f"mshegolev/whilly:{__version__}", help="Whilly Docker image tag.")
    parser.add_argument("--control-url", default="http://host.docker.internal:8000", help="Worker control-plane URL.")
    parser.add_argument("--plan-id", default="demo", help="Initial plan id for the worker to drain.")
    parser.add_argument("--worker-hostname", default="whilly-worker", help="Worker hostname shown in audit logs.")
    parser.add_argument("--jira-url", default="https://your-company.atlassian.net", help="Jira base URL.")
    parser.add_argument("--jira-user", default="you@example.com", help="Jira user email.")
    parser.add_argument("--jira-token", default="CHANGE_ME_JIRA_API_TOKEN", help="Jira API token.")
    parser.add_argument("--bootstrap-token", default=None, help="Control-plane bootstrap token. Generated by default.")
    parser.add_argument("--postgres-password", default=None, help="Postgres password. Generated by default.")
    parser.add_argument("--cli", dest="agent_cli", default="opencode", help="Agent CLI backend for workers.")
    parser.add_argument("--model", default="opencode/big-pickle", help="Agent model for workers.")
    return parser


def _detect_compose_command() -> str:
    if shutil.which("docker"):
        try:
            result = subprocess.run(
                ["docker", "compose", "version"],
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                check=False,
                timeout=5,
            )
        except (OSError, subprocess.TimeoutExpired):
            result = None
        if result is not None and result.returncode == 0:
            return "docker compose"
    if shutil.which("docker-compose"):
        return "docker-compose"
    return "docker compose"


def _check_python_available(
    *,
    err: object,
    which: Callable[[str], str | None] = shutil.which,
    system: str | None = None,
) -> bool:
    """Warn when ``python3.12`` is missing from ``$PATH``.

    Whilly's :file:`pyproject.toml` pins ``requires-python = ">=3.12"``, so the
    host CLI (``pipx install whilly-orchestrator`` / ``whilly`` console script)
    needs a 3.12 interpreter to install at all. Docker workers ship their own
    Python, so we deliberately do not block setup — the generated ``.env``
    files remain useful for the container-only path — but we surface a clear,
    OS-tailored install hint so the operator isn't blindsided later when
    ``pipx`` refuses to resolve a release.

    Returns ``True`` when a suitable interpreter was found, ``False``
    otherwise. The boolean is informational; the caller does not exit on
    ``False``.
    """
    if which(_PYTHON_REQUIRED):
        return True

    err.write(
        f"whilly quick-setup: no '{_PYTHON_REQUIRED}' found on $PATH. "
        f"Whilly requires Python {_MIN_PYTHON_MAJOR}.{_MIN_PYTHON_MINOR}+ for the "
        "host CLI (Docker workers ship their own interpreter).\n"
    )
    err.write("Install Python 3.12 and then `pipx install --python python3.12 whilly-orchestrator`:\n")
    resolved_system = system if system is not None else platform.system()
    if resolved_system == "Darwin":
        err.write("  brew install python@3.12\n")
        err.write("  # or, via pyenv:\n")
        err.write("  pyenv install 3.12 && pyenv local 3.12\n")
    elif resolved_system == "Linux":
        err.write("  # Debian/Ubuntu:\n")
        err.write("  sudo apt update && sudo apt install python3.12 python3.12-venv\n")
        err.write("  # or, via pyenv:\n")
        err.write("  pyenv install 3.12 && pyenv local 3.12\n")
    elif resolved_system == "Windows":
        err.write("  winget install Python.Python.3.12\n")
        err.write("  # or download the official installer from https://www.python.org/downloads/\n")
    else:
        err.write("  Use pyenv or your platform's package manager to install Python 3.12.\n")
    err.flush()
    return False


def _resolve_docker_provider(selected: str) -> str:
    if selected != "auto":
        return selected
    if platform.system() == "Darwin":
        return "colima" if shutil.which("colima") else "desktop"
    return "linux"


def _render_control_env(
    *,
    generated_at: str,
    jira_url: str,
    jira_user: str,
    jira_token: str,
    image: str,
    postgres_password: str,
    bootstrap_token: str,
) -> str:
    return f"""# Generated by whilly quick-setup at {generated_at}
# Local Docker compose config. Do not commit this file.

JIRA_SERVER_URL={jira_url}
JIRA_USERNAME={jira_user}
JIRA_API_TOKEN={jira_token}

WHILLY_BIND_HOST=0.0.0.0
WHILLY_CONTROL_PORT=8000
WHILLY_IMAGE={image}

POSTGRES_USER=whilly
POSTGRES_PASSWORD={postgres_password}
POSTGRES_DB=whilly
POSTGRES_PORT=5432

WHILLY_WORKER_BOOTSTRAP_TOKEN={bootstrap_token}
WHILLY_WORKER_TOKEN=
WHILLY_METRICS_TOKEN=
WHILLY_LOG_LEVEL=INFO
"""


