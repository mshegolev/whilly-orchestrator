def _redact_env(content: str) -> str:
    lines: list[str] = []
    for line in content.splitlines():
        if "=" not in line or line.lstrip().startswith("#"):
            lines.append(line)
            continue
        key, value = line.split("=", 1)
        key = key.strip()
        if key in _GENERATED_SECRET_KEYS:
            lines.append(f"{key}=<generated>")
        elif key in _REDACTED_KEYS and value:
            lines.append(f"{key}=<configured>")
        else:
            lines.append(line)
    return "\n".join(lines) + "\n"


def _write_runbook(
    *,
    out: object,
    repo_root: Path,
    env_content: str,
    worker_env_content: str,
    compose_command: str,
    docker_provider: str,
    print_only: bool,
    show_secrets: bool,
) -> None:
    if print_only:
        out.write("Whilly quick setup preview; no files were written.\n\n")
    else:
        out.write("Whilly quick setup wrote local files:\n")
        out.write(f"  {repo_root / '.env'}\n")
        out.write(f"  {repo_root / '.env.worker'}\n\n")
    out.write(f"Compose command: {compose_command}\n")
    out.write(
        "Secret values are hidden in this terminal output. "
        "Pass --show-secrets only in a private shell if you need to print them.\n\n"
        if not show_secrets
        else "Secret values are shown because --show-secrets was passed.\n\n"
    )
    rendered_env = env_content if show_secrets else _redact_env(env_content)
    rendered_worker_env = worker_env_content if show_secrets else _redact_env(worker_env_content)
    out.write("# .env\n")
    out.write(rendered_env)
    out.write("\n# .env.worker\n")
    out.write(rendered_worker_env)
    out.write("\n")

    if docker_provider == "colima":
        out.write("macOS Colima prep:\n")
        out.write("  colima start\n")
        out.write("  docker context use colima\n")
        out.write("  export DOCKER_HOST=\"$(docker context inspect colima --format '{{.Endpoints.docker.Host}}')\"\n")
        out.write("  export TESTCONTAINERS_RYUK_DISABLED=true\n\n")
    elif docker_provider == "desktop":
        out.write("macOS Docker Desktop prep:\n")
        out.write("  open -a Docker\n")
        out.write("  docker ps\n\n")

    out.write("Start control plane:\n")
    out.write(f"  {compose_command} --env-file .env -f {CONTROL_COMPOSE_FILE} up -d\n")
    out.write(f"  {compose_command} --env-file .env -f {CONTROL_COMPOSE_FILE} ps\n")
    out.write("  curl -fsS http://127.0.0.1:8000/health\n\n")
    out.write("Start worker:\n")
    out.write(f"  {compose_command} --env-file .env.worker -f {WORKER_COMPOSE_FILE} up -d\n")
    out.write(f"  {compose_command} --env-file .env.worker -f {WORKER_COMPOSE_FILE} logs -f worker\n\n")
    out.write("Import a Jira issue when credentials are configured:\n")
    out.write(
        f"  {compose_command} --env-file .env -f {CONTROL_COMPOSE_FILE} exec "
        "-e JIRA_SERVER_URL -e JIRA_USERNAME -e JIRA_API_TOKEN "
        "control-plane whilly jira intake ABC-123\n"
    )
    out.flush()


def run_quick_setup_command(
    argv: Sequence[str],
    *,
    stdout: object | None = None,
    stderr: object | None = None,
    compose_probe: Callable[[], str] = _detect_compose_command,
    token_factory: Callable[[int], str] = secrets.token_urlsafe,
    python_probe: Callable[[str], str | None] = shutil.which,
) -> int:
    out = sys.stdout if stdout is None else stdout
    err = sys.stderr if stderr is None else stderr
    parser = _build_parser()
    args = parser.parse_args(list(argv))

    # Surface the Python 3.12 install hint up front. Soft check: we keep
    # going so the operator still gets ``.env`` / ``.env.worker`` for the
    # Docker-only path (workers ship their own interpreter).
    _check_python_available(err=err, which=python_probe)

    repo_root = Path(args.repo_root).expanduser().resolve()
    if not repo_root.exists() or not repo_root.is_dir():
        err.write(f"whilly quick-setup: repo root does not exist or is not a directory: {repo_root}\n")
        err.flush()
        return EXIT_USAGE

    if not args.yes and not args.print_only:
        err.write("whilly quick-setup: pass --yes to write files, or --print-only to preview.\n")
        err.flush()
        return EXIT_USAGE

    env_path = repo_root / ".env"
    worker_env_path = repo_root / ".env.worker"
    existing = [path for path in (env_path, worker_env_path) if path.exists()]
    if existing and not args.force and not args.print_only:
        rendered = ", ".join(path.name for path in existing)
        err.write(f"whilly quick-setup: {rendered} already exists; pass --force to overwrite.\n")
        err.flush()
        return EXIT_USAGE

    bootstrap_token = args.bootstrap_token or token_factory(24)
    postgres_password = args.postgres_password or token_factory(18)
    generated_at = datetime.now(UTC).isoformat(timespec="seconds")
    env_content = _render_control_env(
        generated_at=generated_at,
        jira_url=args.jira_url,
        jira_user=args.jira_user,
        jira_token=args.jira_token,
        image=args.image,
        postgres_password=postgres_password,
        bootstrap_token=bootstrap_token,
    )
    worker_env_content = _render_worker_env(
        generated_at=generated_at,
        control_url=args.control_url,
        image=args.image,
        bootstrap_token=bootstrap_token,
        worker_hostname=args.worker_hostname,
        plan_id=args.plan_id,
        agent_cli=args.agent_cli,
        model=args.model,
    )

    compose_command = args.compose_command or compose_probe() or "docker compose"
    docker_provider = _resolve_docker_provider(args.docker_provider)

    if not args.print_only:
        env_path.write_text(env_content, encoding="utf-8")
        worker_env_path.write_text(worker_env_content, encoding="utf-8")

    _write_runbook(
        out=out,
        repo_root=repo_root,
        env_content=env_content,
        worker_env_content=worker_env_content,
        compose_command=compose_command,
        docker_provider=docker_provider,
        print_only=bool(args.print_only),
        show_secrets=bool(args.show_secrets),
    )
    return EXIT_OK
