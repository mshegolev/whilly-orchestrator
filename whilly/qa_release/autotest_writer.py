def test_release_plan_has_requirement_coverage():
    requirement_ids = {{requirement["id"] for requirement in QA_TEST_PLAN["requirements"]}}
    covered_ids = {{
        requirement_id
        for test_case in QA_TEST_PLAN["test_cases"]
        for requirement_id in test_case["source_requirement_ids"]
    }}

    assert requirement_ids
    assert requirement_ids <= covered_ids


