    assert repo_hints, "release must have at least one implementation or deployment repository hint"
    assert all(repo["provider"] for repo in repo_hints)
    assert all(repo["repo_full_name"] for repo in repo_hints)
    assert any(repo["ref"] for repo in repo_hints), "at least one repo hint must pin tag, branch, or commit"


