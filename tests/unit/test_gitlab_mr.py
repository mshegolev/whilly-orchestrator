# ── Fixtures ───────────────────────────────────────────────────────────────────


def _make_task(
    task_id: str = "TASK-001",
    description: str = "Implement the feature",
    acceptance_criteria: list[str] | None = None,
    test_steps: list[str] | None = None,
) -> Task:
    return Task(
        id=task_id,
        phase="build",
        category="feature",
        priority="high",
        description=description,
        status="done",
        acceptance_criteria=acceptance_criteria or ["AC-1", "AC-2"],
        test_steps=test_steps or ["Run pytest"],
    )


def _completed_process(returncode: int = 0, stdout: str = "", stderr: str = "") -> subprocess.CompletedProcess:
    return subprocess.CompletedProcess(args=[], returncode=returncode, stdout=stdout, stderr=stderr)


# ── _extract_mr_number_from_url ────────────────────────────────────────────────


@pytest.mark.parametrize(
    "url,expected",
    [
        ("https://gitlab.services.mts.ru/foo/bar/-/merge_requests/42", 42),
        ("https://gitlab.example.com/group/project/-/merge_requests/7", 7),
        ("not-a-url", None),
        ("", None),
    ],
)
def test_extract_mr_number_from_url(url: str, expected: int | None) -> None:
    assert _extract_mr_number_from_url(url) == expected


# ── _branch_name ──────────────────────────────────────────────────────────────


def test_branch_name_default_prefix() -> None:
    task = _make_task("TASK-42")
    assert _branch_name(task, "whilly") == "whilly/TASK-42"


def test_branch_name_sanitises_spaces() -> None:
    task = _make_task("my task id")
    result = _branch_name(task, "whilly")
    assert " " not in result
    assert result.startswith("whilly/")


