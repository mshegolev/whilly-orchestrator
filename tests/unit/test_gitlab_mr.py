# ── MR create failures ────────────────────────────────────────────────────────


def test_open_mr_for_task_mr_create_timeout(tmp_path: Path) -> None:
    task = _make_task()
    push_ok = _completed_process(returncode=0)

    def _side_effect(*args, **kwargs):
        cmd = args[0] if args else kwargs.get("args", [])
        if "push" in cmd:
            return push_ok
        raise subprocess.TimeoutExpired(cmd="glab mr create", timeout=60)

    with patch("subprocess.run", side_effect=_side_effect):
        result = open_mr_for_task(task=task, worktree_path=tmp_path)

    assert result.ok is False
    assert result.failure_mode == "mr_create_timeout"


def test_open_mr_for_task_mr_create_failed(tmp_path: Path) -> None:
    task = _make_task()
    push_ok = _completed_process(returncode=0)
    mr_fail = _completed_process(returncode=1, stderr="fatal: repository not found", stdout="")

    with patch("subprocess.run", side_effect=[push_ok, mr_fail]):
        result = open_mr_for_task(task=task, worktree_path=tmp_path)

    assert result.ok is False
    assert result.failure_mode == "mr_create_failed"
    assert result.gh_exit_code == 1
    assert "glab mr create failed" in result.reason


def test_open_mr_for_task_mr_already_exists_extracts_url(tmp_path: Path) -> None:
    task = _make_task()
    push_ok = _completed_process(returncode=0)
    existing_url = "https://gitlab.services.mts.ru/foo/bar/-/merge_requests/5"
    mr_exists = _completed_process(
        returncode=1,
        stderr=f"A merge request already exists for this branch: {existing_url}",
        stdout="",
    )

    with patch("subprocess.run", side_effect=[push_ok, mr_exists]):
        result = open_mr_for_task(task=task, worktree_path=tmp_path)

    assert result.ok is True
    assert result.reason == "mr already existed"
    assert result.pr_number == 5


