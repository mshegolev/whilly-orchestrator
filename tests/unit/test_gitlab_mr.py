# ── _resolve_gitlab_token ─────────────────────────────────────────────────────


def test_resolve_gitlab_token_from_env_var() -> None:
    env = {"GITLAB_API_TOKEN": "secret-token"}
    assert _resolve_gitlab_token("gitlab.example.com", env=env) == "secret-token"


def test_resolve_gitlab_token_whilly_prefix_fallback() -> None:
    env = {"WHILLY_GITLAB_API_TOKEN": "whilly-token"}
    assert _resolve_gitlab_token("gitlab.example.com", env=env) == "whilly-token"


def test_resolve_gitlab_token_env_takes_priority_over_glab_cli() -> None:
    env = {"GITLAB_API_TOKEN": "env-token"}
    # glab should NOT be called when the env var is present
    with patch("subprocess.run") as mock_run:
        result = _resolve_gitlab_token("gitlab.example.com", env=env)
    assert result == "env-token"
    mock_run.assert_not_called()


def test_resolve_gitlab_token_glab_cli_fallback() -> None:
    env: dict[str, str] = {}
    glab_response = _completed_process(returncode=0, stdout="cli-token\n")
    with patch("subprocess.run", return_value=glab_response) as mock_run:
        result = _resolve_gitlab_token("gitlab.services.mts.ru", env=env)
    assert result == "cli-token"
    mock_run.assert_called_once()
    args = mock_run.call_args[0][0]
    assert "glab" in args
    assert "gitlab.services.mts.ru" in args


def test_resolve_gitlab_token_returns_none_when_both_missing() -> None:
    env: dict[str, str] = {}
    glab_response = _completed_process(returncode=1, stdout="", stderr="not configured")
    with patch("subprocess.run", return_value=glab_response):
        result = _resolve_gitlab_token("gitlab.example.com", env=env)
    assert result is None


def test_resolve_gitlab_token_returns_none_when_glab_raises() -> None:
    env: dict[str, str] = {}
    with patch("subprocess.run", side_effect=FileNotFoundError("glab not found")):
        result = _resolve_gitlab_token("gitlab.example.com", env=env)
    assert result is None


# ── _infer_remote_host ────────────────────────────────────────────────────────


def test_infer_remote_host_https_url(tmp_path: Path) -> None:
    git_response = _completed_process(returncode=0, stdout="https://gitlab.services.mts.ru/foo/bar.git\n")
    with patch("subprocess.run", return_value=git_response):
        host = _infer_remote_host(tmp_path)
